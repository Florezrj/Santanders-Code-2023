
public class VariaveisPrimitivas {
    public static void main(String[] args) {
        // 5 caracteristicas do metodo main
        // 1 - public: todo projeto pode consultar
        // 2 - static: é estatico
        // 3 - void: que ele não retorna valor nenhum
        // 4 - main: obrigatorio
        // 5 - Parametros: arrays ou varargs de string.

        // tipos primitivos
        // byte: -128 a 127
        // short: -32768 a 32767
        // char: 0 a 65535
        // int: -2b a 2b
        // long: -9t a 9t

        byte variavelByte = 124;
        System.out.println(variavelByte);

    }
}