public class Lacos_numericos {

    public static void main(String[] args) {
        // Para imprimir o número 1 até o número 10, faça
        for (int i = 1; i <= 10; i++) {
            for (int j = 1; j <= 10; j++) {
                System.out.printf("%s x %s = %s", i, j, i*j);
                System.out.println("");
            }
        }

    }
}
